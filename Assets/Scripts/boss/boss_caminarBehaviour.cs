using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class boss_caminarBehaviour : StateMachineBehaviour
{
    private boss1 boss;
    private Transform jugador;

    [SerializeField] private float velocidad;


    // OnStateEnter is called when a transition starts and the state machine starts to evaluate this state
    override public void OnStateEnter(Animator animator, AnimatorStateInfo stateInfo, int layerIndex)
    {
        jugador = GameObject.Find("Meincharacte").GetComponent<Transform>();
        boss = animator.gameObject.GetComponent<boss1>();
    }

    // OnStateUpdate is called on each Update frame between OnStateEnter and OnStateExit callbacks
    override public void OnStateUpdate(Animator animator, AnimatorStateInfo stateInfo, int layerIndex)
    {
        animator.transform.position = Vector2.MoveTowards(animator.transform.position, jugador.position, velocidad * Time.deltaTime);
        boss.Girar();
    }

    // OnStateExit is called when a transition ends and the state machine finishes evaluating this state
    override public void OnStateExit(Animator animator, AnimatorStateInfo stateInfo, int layerIndex)
    {

    }

